package com.songxinjing.flyfish.plugin.joom.exception;

public class JoomException extends Exception {

	private static final long serialVersionUID = 1L;

	public JoomException() {
		super();
	}

	public JoomException(String message, Throwable cause) {
		super(message, cause);
	}

	public JoomException(String message) {
		super(message);
	}

	public JoomException(Throwable cause) {
		super(cause);
	}

}
